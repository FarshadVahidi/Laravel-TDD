     */
    public function destroy(Book $book)
    {
        $book->delete();

        return redirect('/books');
    }

    /**
     * @return array
     */
    public function validateRequest(): array
    {
        return request()->validate([
            'title' => 'required|string|max:255',
            'author' => 'required|string|max:255',
        ]);
    }
